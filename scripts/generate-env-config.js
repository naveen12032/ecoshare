#!/usr/bin/env node
/**
 * EcoShare — Environment Config Generator
 * ----------------------------------------
 * Reads .env and generates two files:
 *   1. src/env-config.js     → sets window.__ENV__ for browser/Capacitor use
 *   2. firebase-config.json  → consumed by the existing firebase-config.js loader
 *
 * Run manually:   node scripts/generate-env-config.js
 * Run via npm:    npm run env:generate
 */

const fs = require('fs');
const path = require('path');

const ROOT = path.resolve(__dirname, '..');
const ENV_FILE = path.join(ROOT, '.env');
const OUTPUT_JS = path.join(ROOT, 'src', 'env-config.js');
const OUTPUT_FIREBASE_JSON = path.join(ROOT, 'firebase-config.json');

// ---------------------------------------------------------------------------
// 1. Parse .env file (no external dependencies needed)
// ---------------------------------------------------------------------------
function parseEnv(filePath) {
  if (!fs.existsSync(filePath)) {
    console.warn('[env-config] WARNING: .env file not found at', filePath);
    console.warn('[env-config] Using empty config. Copy .env.example → .env and fill in values.');
    return {};
  }

  const raw = fs.readFileSync(filePath, 'utf8');
  const env = {};

  for (const line of raw.split('\n')) {
    const trimmed = line.trim();
    // Skip blank lines and comments
    if (!trimmed || trimmed.startsWith('#')) continue;

    const eqIdx = trimmed.indexOf('=');
    if (eqIdx === -1) continue;

    const key = trimmed.slice(0, eqIdx).trim();
    const value = trimmed.slice(eqIdx + 1).trim()
      // Strip optional surrounding quotes
      .replace(/^["']|["']$/g, '');

    env[key] = value;
  }

  return env;
}

// ---------------------------------------------------------------------------
// 2. Validate required keys
// ---------------------------------------------------------------------------
const REQUIRED_KEYS = [
  'SUPABASE_URL',
  'SUPABASE_ANON_KEY',
];

const FIREBASE_KEYS = [
  'FIREBASE_API_KEY',
  'FIREBASE_AUTH_DOMAIN',
  'FIREBASE_PROJECT_ID',
  'FIREBASE_STORAGE_BUCKET',
  'FIREBASE_MESSAGING_SENDER_ID',
  'FIREBASE_APP_ID',
];

function validate(env) {
  const missing = REQUIRED_KEYS.filter(k => !env[k]);
  if (missing.length > 0) {
    console.warn('[env-config] WARNING: Missing required env keys:', missing.join(', '));
  }
}

// ---------------------------------------------------------------------------
// 3. Generate src/env-config.js
// ---------------------------------------------------------------------------
function generateBrowserConfig(env) {
  const lines = [
    '// AUTO-GENERATED — DO NOT EDIT MANUALLY',
    '// Generated by: scripts/generate-env-config.js',
    '// Source: .env',
    '// This file is gitignored. Regenerate with: npm run env:generate',
    '',
    '(function () {',
    '  window.__ENV__ = {',
    `    SUPABASE_URL: ${JSON.stringify(env.SUPABASE_URL || '')},`,
    `    SUPABASE_ANON_KEY: ${JSON.stringify(env.SUPABASE_ANON_KEY || '')},`,
    `    FIREBASE_API_KEY: ${JSON.stringify(env.FIREBASE_API_KEY || '')},`,
    `    FIREBASE_AUTH_DOMAIN: ${JSON.stringify(env.FIREBASE_AUTH_DOMAIN || '')},`,
    `    FIREBASE_PROJECT_ID: ${JSON.stringify(env.FIREBASE_PROJECT_ID || '')},`,
    `    FIREBASE_STORAGE_BUCKET: ${JSON.stringify(env.FIREBASE_STORAGE_BUCKET || '')},`,
    `    FIREBASE_MESSAGING_SENDER_ID: ${JSON.stringify(env.FIREBASE_MESSAGING_SENDER_ID || '')},`,
    `    FIREBASE_APP_ID: ${JSON.stringify(env.FIREBASE_APP_ID || '')},`,
    `    GEMINI_API_KEY: ${JSON.stringify(env.GEMINI_API_KEY || '')},`,
    '  };',
    '  console.log("[EcoShare] Environment config loaded. Provider keys present:", {',
    '    supabase: !!(window.__ENV__.SUPABASE_URL && window.__ENV__.SUPABASE_ANON_KEY),',
    '    firebase: !!(window.__ENV__.FIREBASE_API_KEY && window.__ENV__.FIREBASE_PROJECT_ID),',
    '    gemini: !!window.__ENV__.GEMINI_API_KEY,',
    '  });',
    '})();',
  ];

  fs.writeFileSync(OUTPUT_JS, lines.join('\n'), 'utf8');
  console.log('[env-config] ✅ Generated:', OUTPUT_JS);
}

// ---------------------------------------------------------------------------
// 4. Regenerate firebase-config.json from .env values
// ---------------------------------------------------------------------------
function generateFirebaseJson(env) {
  const hasFirebase = FIREBASE_KEYS.every(k => env[k]);
  if (!hasFirebase) {
    console.log('[env-config] ⚠️  Skipping firebase-config.json — Firebase keys not all present in .env');
    return;
  }

  const config = {
    apiKey: env.FIREBASE_API_KEY,
    authDomain: env.FIREBASE_AUTH_DOMAIN,
    projectId: env.FIREBASE_PROJECT_ID,
    storageBucket: env.FIREBASE_STORAGE_BUCKET,
    messagingSenderId: env.FIREBASE_MESSAGING_SENDER_ID,
    appId: env.FIREBASE_APP_ID,
  };

  fs.writeFileSync(OUTPUT_FIREBASE_JSON, JSON.stringify(config, null, 2), 'utf8');
  console.log('[env-config] ✅ Generated:', OUTPUT_FIREBASE_JSON);
}

// ---------------------------------------------------------------------------
// Main
// ---------------------------------------------------------------------------
console.log('[env-config] Reading .env from', ENV_FILE);
const env = parseEnv(ENV_FILE);
validate(env);
generateBrowserConfig(env);
generateFirebaseJson(env);
console.log('[env-config] Done.');
